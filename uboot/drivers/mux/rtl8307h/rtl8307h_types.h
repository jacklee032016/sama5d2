enum PORT_MASK
{
    PM_PORT_0 = (1 << PN_PORT0),
    PM_PORT_1 = (1 << PN_PORT1),
    PM_PORT_2 = (1 << PN_PORT2),
    PM_PORT_3 = (1 << PN_PORT3),
    PM_PORT_4 = (1 << PN_PORT4),
    PM_PORT_5 = (1 << PN_PORT5),
    PM_PORT_6 = (1 << PN_PORT6),
    PM_PORT_7 = (1 << PN_PORT7),
    PM_PORT_ALL = 0xff,
};

#define IS_VALID_PORT_ID(portId)            (((portId) < PN_PORT_END ) ? 1 : 0)
#define IS_VALID_PORT_MASK(portMask)        (((portMask) & PM_PORT_ALL) ? 1 : 0)
#define IS_VALID_PHY_ID(phyId)              ((((phyId) == 4) || ((phyId) == 6)) ? 1 : 0)
#define IS_VALID_HEAC_PORT_ID(portId)       ((((portId) >= 0 && (portId) <= 3) || ((portId) == 6)) ? 1 : 0)


